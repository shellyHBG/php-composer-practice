<?php

    namespace Math;
    class CustomizedMath {

        function Add($a, $b) {
            $rst = $a+$b;
            echo $a . " + " . $b . " = " . $rst . "<br>";
        }

        function Sub($a, $b) {
            $rst = $a-$b;
            echo $a . " - " . $b . " = " . $rst . "<br>";
        }
    }
?>